    // Get current language - 默认英文
    function getLanguage() {
        const stored = localStorage.getItem(STORAGE_KEY);
        if (!stored) {
            localStorage.setItem(STORAGE_KEY, DEFAULT_LANG);
            return DEFAULT_LANG;
        }
        return stored;
    }

    // Set language
    function setLanguage(lang) {
        localStorage.setItem(STORAGE_KEY, lang);
        document.documentElement.lang = lang === 'zh' ? 'zh-CN' : 'en';
        applyTranslations(lang);
        updateDropdownState(lang);
    }

    // Apply translations
    function applyTranslations(lang) {
        const t = i18n[lang];
        
        // Update page title
        document.title = t.siteTitle;
        
        // Site title and description
        const siteTitle = document.querySelector('.site-title a');
        const siteDesc = document.querySelector('.site-description');
        if (siteTitle) siteTitle.textContent = t.siteTitle;
        if (siteDesc) siteDesc.textContent = t.siteDesc;

        // Navigation
        const navLinks = document.querySelectorAll('.site-nav a');
        navLinks.forEach(link => {
            const href = link.getAttribute('href');
            if (href && (href.includes('/index') || href === '/' || href.endsWith('/gamedev-tech-daily/'))) {
                link.textContent = t.navHome;
            } else if (href && href.includes('/search')) {
                link.textContent = t.navSearch;
            } else if (href && href.includes('/categories')) {
                link.textContent = t.navCategories;
            } else if (href && href.includes('/feed.xml')) {
                link.textContent = t.navRSS;
            }
        });

        // Section titles
        const sectionTitles = document.querySelectorAll('.section-title');
        sectionTitles.forEach(title => {
            const text = title.textContent.trim();
            if (text.includes('头条') || text.includes('Headlines')) {
                title.textContent = t.todayHeadlines;
            } else if (text.includes('历史') || text.includes('Archive')) {
                title.textContent = t.archive;
            } else if (text.includes('分类') || text.includes('Categories')) {
                title.textContent = t.navCategories;
            } else if (text.includes('搜索') || text.includes('Search')) {
                title.textContent = t.navSearch;
            }
        });

        // More link
        const moreLink = document.querySelector('.more-link');
        if (moreLink) moreLink.textContent = t.more;

        // Back link
        const backLink = document.querySelector('.back-link');
        if (backLink) backLink.textContent = t.backToHome;

        // Footer
        const footerText = document.querySelector('.site-footer p');
        if (footerText) {
            const year = new Date().getFullYear();
            footerText.innerHTML = `© ${year} ${t.siteTitle} | ${t.footerText}`;
        }

        // Translate category tags
        translateCategoryTags(lang, t);

        // Translate article content
        translateArticleContent(lang, t);

        // Update date display
        updateDateDisplay(lang, t);
    }

    // Translate category tags
    function translateCategoryTags(lang, t) {
        const categoryTags = document.querySelectorAll('.article-category, .category-tag');
        categoryTags.forEach(tag => {
            const text = tag.textContent.replace('#', '').trim();
            if (t.categories[text]) {
                tag.textContent = '#' + t.categories[text];
            }
        });
    }

